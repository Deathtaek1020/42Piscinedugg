#include <unistd.h>

int	ft_sqrt(int nb)
{
	long	i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i++ <= 46340)
	{
		if (i * i == nb)
			return (i);
	}
	return (0);
}
